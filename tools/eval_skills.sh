#!/usr/bin/env bash
# eval_skills.sh — Validate that cute-pixel-* skills aren't drifting from
# the current project architecture.
#
# What this catches:
#   1. cute-pixel-module-gen lost its ground truth (lib/features/_template/ deleted/broken)
#   2. SKILL.md frontmatter `name:` no longer matches its directory
#   3. The cp + sed mechanical flow described in module-gen no longer
#      produces filename-conforming, leftover-free modules
#
# What this does NOT catch (out of scope, use cute-pixel-review for those):
#   - Whether SKILL.md content is still accurate vs current docs
#   - Whether skills cover the right scenarios
#   - Quality of generated test cases
#
# Run via: `make eval-skills`. Exit 0 = clean, 1 = at least one drift.
#
# Safe by design: never writes inside lib/ or .claude/. All scratch work
# happens in $(mktemp -d), cleaned up via trap on exit/interrupt.

set -u

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "$SCRIPT_DIR/.." && pwd)"
SKILL_DIR="$REPO_ROOT/.claude/skills"
TEMPLATE_DIR="$REPO_ROOT/lib/features/_template"

FAILS=0
fail() { echo "[FAIL] $*" >&2; FAILS=$((FAILS + 1)); }
ok()   { echo "[OK]   $*"; }

# Detect platform for sed -i (macOS needs '' arg, Linux doesn't).
case "$(uname)" in
  Darwin) SED_INPLACE=(sed -i '') ;;
  *)      SED_INPLACE=(sed -i)    ;;
esac

# ---- Check 1: cute-pixel-module-gen ground truth -----------------------------
if [ ! -d "$TEMPLATE_DIR" ]; then
  fail "cute-pixel-module-gen: ground truth lib/features/_template/ missing"
else
  ok "ground truth exists: lib/features/_template/"
fi

if [ ! -d "$REPO_ROOT/test/features/_template" ]; then
  fail "cute-pixel-module-gen: tests for _template/ missing (test/features/_template/)"
else
  ok "ground truth tests exist: test/features/_template/"
fi

# ---- Check 1b: assets/ ground truth (each type has _template/ skeleton) ------
# The cute-pixel-module-gen Step 5 (sprite接入) and assets/README.md navigator
# both depend on every assets/{type}/_template/ existing with a README.
# Regression here means new modules can't reliably cp from a known-good template.
ASSETS_DIR="$REPO_ROOT/assets"
declare -a ASSET_TEMPLATES=(
  "sprites/_template"
  "items/_template"
  "ui/buttons/_template"
  "ui/icons/_template"
  "ui/frames/_template"
  "scenes/_template"
  "effects/_template"
  "tilemaps/_template"
  "audio/sfx/_template"
  "audio/music/_template"
  "fonts/_template"
)
asset_template_fails=0
for t in "${ASSET_TEMPLATES[@]}"; do
  dir="$ASSETS_DIR/$t"
  if [ ! -d "$dir" ]; then
    fail "assets ground truth: $t missing"
    asset_template_fails=$((asset_template_fails + 1))
    continue
  fi
  if [ ! -f "$dir/README.md" ]; then
    fail "assets ground truth: $t/README.md missing (every type template needs a README)"
    asset_template_fails=$((asset_template_fails + 1))
  fi
done
[ "$asset_template_fails" -eq 0 ] && ok "assets ground truth: all ${#ASSET_TEMPLATES[@]} type templates present with README"

# ---- Check 2: each skill's frontmatter name matches its dir ------------------
for skill_dir in "$SKILL_DIR"/cute-pixel-*/; do
  [ -d "$skill_dir" ] || continue
  skill_name="$(basename "$skill_dir")"
  skill_md="$skill_dir/SKILL.md"
  if [ ! -f "$skill_md" ]; then
    fail "$skill_name: SKILL.md missing"
    continue
  fi
  declared_name="$(grep -E '^name:' "$skill_md" | head -n1 | sed -E 's/^name:[[:space:]]*//')"
  if [ "$declared_name" != "$skill_name" ]; then
    fail "$skill_name: frontmatter name='$declared_name' does not match dir name"
  else
    ok "$skill_name: frontmatter name matches dir"
  fi
done

# ---- Check 3: cp + sed mechanical flow produces conforming module ------------
# This simulates the cute-pixel-module-gen Step 2/3 (cp _template + sed).
# We do it in a tmpdir so lib/ stays untouched.
if [ -d "$TEMPLATE_DIR" ]; then
  TMPDIR_EVAL="$(mktemp -d 2>/dev/null || mktemp -d -t eval_skills)"
  trap 'rm -rf "$TMPDIR_EVAL"' EXIT INT TERM

  TARGET_NAME="eval_test"
  TARGET_PASCAL="EvalTest"
  TARGET_DIR="$TMPDIR_EVAL/$TARGET_NAME"

  cp -R "$TEMPLATE_DIR" "$TARGET_DIR"

  # Rename _template_*.dart → eval_test_*.dart at depth 1.
  for f in "$TARGET_DIR"/_template_*.dart; do
    [ -e "$f" ] || continue
    base="$(basename "$f")"
    new_base="${base/_template/$TARGET_NAME}"
    mv "$f" "$TARGET_DIR/$new_base"
  done

  # sed replace inside .dart files (skip generated products — they'll be
  # regenerated by `make codegen` in the real flow, content not relevant here).
  find "$TARGET_DIR" -type f -name '*.dart' \
    ! -name '*.freezed.dart' ! -name '*.g.dart' \
    -exec "${SED_INPLACE[@]}" "s/_template/$TARGET_NAME/g" {} \;
  find "$TARGET_DIR" -type f -name '*.dart' \
    ! -name '*.freezed.dart' ! -name '*.g.dart' \
    -exec "${SED_INPLACE[@]}" "s/Template/$TARGET_PASCAL/g" {} \;
  # 3rd sed (mirrors module-gen SKILL.md Step 3): strip the 8-line ⚠️ TEMPLATE
  # warning header from the cp'd files. Marker line stays uppercase TEMPLATE
  # (untouched by the two sed runs above), so this still finds it.
  find "$TARGET_DIR" -type f -name '*.dart' \
    ! -name '*.freezed.dart' ! -name '*.g.dart' \
    -exec "${SED_INPLACE[@]}" '/^\/\/ ⚠️ TEMPLATE/,/^$/d' {} \;

  # Verify rule 4: every top-level .dart file matches {module}_*.dart.
  rule4_viol=0
  for f in "$TARGET_DIR"/*.dart; do
    [ -e "$f" ] || continue
    base="$(basename "$f")"
    case "$base" in
      "${TARGET_NAME}_"*) ;;
      *)
        fail "cp+sed flow: '$base' violates {module}_*.dart (rule 4)"
        rule4_viol=$((rule4_viol + 1))
        ;;
    esac
  done
  [ "$rule4_viol" -eq 0 ] && ok "cp+sed flow: all top-level files match $TARGET_NAME""_*.dart"

  # Verify no leftover _template / Template / TEMPLATE / ⚠️ tokens in non-generated source.
  # Includes uppercase TEMPLATE and the ⚠️ warning marker, which are stripped
  # by the 3rd sed (deletes the 8-line warning block from the cp'd module).
  leftover_files="$(
    find "$TARGET_DIR" -type f -name '*.dart' \
      ! -name '*.freezed.dart' ! -name '*.g.dart' \
      -exec grep -lE '_template|Template|TEMPLATE|⚠️' {} \; 2>/dev/null
  )"
  if [ -n "$leftover_files" ]; then
    while IFS= read -r f; do
      fail "cp+sed flow: leftover _template/Template/TEMPLATE/⚠️ token in $(basename "$f")"
    done <<< "$leftover_files"
  else
    ok "cp+sed flow: no leftover _template/Template/TEMPLATE/⚠️ tokens"
  fi

  # Verify package import prefix preserved.
  bad_imports="$(
    grep -rEhn "^import 'package:cute_pixel/features/_template/" "$TARGET_DIR" 2>/dev/null || true
  )"
  if [ -n "$bad_imports" ]; then
    fail "cp+sed flow: imports still reference features/_template/ (sed missed them):\n$bad_imports"
  else
    ok "cp+sed flow: imports correctly rewritten to features/$TARGET_NAME/"
  fi
fi

# ---- Summary -----------------------------------------------------------------
echo ""
if [ "$FAILS" -gt 0 ]; then
  echo "eval_skills: $FAILS failure(s)" >&2
  exit 1
fi
echo "eval_skills: all checks passed"
exit 0
